def _now():
    return datetime.now(timezone.utc)


def _connect(conninfo, *, autocommit=False):
    require_psycopg()
    return cast(Any, psycopg.connect(
        conninfo, autocommit=autocommit,
        row_factory=cast(Any, dict_row)))


